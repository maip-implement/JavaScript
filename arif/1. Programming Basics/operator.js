// ******* Arithmetic Operator (+, -, *, / %, ++, --) *******
console.log(11 % 2) // 1

// Pre Incremental and Pre Decremental
let numPre = 10
console.log(++numPre) //11
console.log(numPre) //11
console.log(--numPre) //10
console.log(numPre) //10

// Post Incremental and Pre Decremental
let numPost = 10
console.log(numPost++) //10
console.log(numPost) //11
console.log(numPost--) //11
console.log(numPost) //10




// ******* Assignment Operator (+=, -=, *=, /=, %=) *******
let a = 5
a += 2
console.log(a)  //7




// ******* Comparison Operator (==, ===, !=, !==, >, >=, <, <=) *******
console.log(5 == '5') // true
console.log(5 === '5') // false

console.log(5 >= 5) // true
console.log(5 >= '5') // true
